package com.chingtech.carlogo.model;

public class Aircondrefrigerator {

    /** 空调控制方式 */
    private String airconditioningcontrolmode;
    /** 温度分区控制 */
    private String tempzonecontrol;
    /** 后排独立空调 */
    private String rearairconditioning;
    /** 后排出风口 */
    private String reardischargeoutlet;
    /** 空气调节/花粉过滤 */
    private String airconditioning;
    /** 车内空气净化装置 */
    private String airpurifyingdevice;
    /** 车载冰箱 */
    private String carrefrigerator;

    public String getAirconditioningcontrolmode() {
        return airconditioningcontrolmode;
    }

    public void setAirconditioningcontrolmode(String airconditioningcontrolmode) {
        this.airconditioningcontrolmode = airconditioningcontrolmode;
    }

    public String getTempzonecontrol() {
        return tempzonecontrol;
    }

    public void setTempzonecontrol(String tempzonecontrol) {
        this.tempzonecontrol = tempzonecontrol;
    }

    public String getRearairconditioning() {
        return rearairconditioning;
    }

    public void setRearairconditioning(String rearairconditioning) {
        this.rearairconditioning = rearairconditioning;
    }

    public String getReardischargeoutlet() {
        return reardischargeoutlet;
    }

    public void setReardischargeoutlet(String reardischargeoutlet) {
        this.reardischargeoutlet = reardischargeoutlet;
    }

    public String getAirconditioning() {
        return airconditioning;
    }

    public void setAirconditioning(String airconditioning) {
        this.airconditioning = airconditioning;
    }

    public String getAirpurifyingdevice() {
        return airpurifyingdevice;
    }

    public void setAirpurifyingdevice(String airpurifyingdevice) {
        this.airpurifyingdevice = airpurifyingdevice;
    }

    public String getCarrefrigerator() {
        return carrefrigerator;
    }

    public void setCarrefrigerator(String carrefrigerator) {
        this.carrefrigerator = carrefrigerator;
    }

}
