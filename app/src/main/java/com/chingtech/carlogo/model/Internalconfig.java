public class Internalconfig {

	/** 方向盘前后调节 */
	private String steeringwheelbeforeadjustment;
	/** 方向盘上下调节 */
	private String steeringwheelupadjustment;
	/** 方向盘调节方式 */
	private String steeringwheeladjustmentmode;
	/** 方向盘记忆设置 */
	private String steeringwheelmemory;
	/** 方向盘表面材料 */
	private String steeringwheelmaterial;
	/** 多功能方向盘 */
	private String steeringwheelmultifunction;
	/** 方向盘加热 */
	private String steeringwheelheating;
	/** 行车电脑显示屏 */
	private String computerscreen;
	/** HUD抬头数字显示 */
	private String huddisplay;
	/** 内饰颜色 */
	private String interiorcolor;
	/** 后排杯架 */
	private String rearcupholder;
	/** 车内电源电压 */
	private String supplyvoltage;

	public String getSteeringwheelbeforeadjustment() {
		return steeringwheelbeforeadjustment;
	}

	public void setSteeringwheelbeforeadjustment(String steeringwheelbeforeadjustment) {
		this.steeringwheelbeforeadjustment = steeringwheelbeforeadjustment;
	}

	public String getSteeringwheelupadjustment() {
		return steeringwheelupadjustment;
	}

	public void setSteeringwheelupadjustment(String steeringwheelupadjustment) {
		this.steeringwheelupadjustment = steeringwheelupadjustment;
	}

	public String getSteeringwheeladjustmentmode() {
		return steeringwheeladjustmentmode;
	}

	public void setSteeringwheeladjustmentmode(String steeringwheeladjustmentmode) {
		this.steeringwheeladjustmentmode = steeringwheeladjustmentmode;
	}

	public String getSteeringwheelmemory() {
		return steeringwheelmemory;
	}

	public void setSteeringwheelmemory(String steeringwheelmemory) {
		this.steeringwheelmemory = steeringwheelmemory;
	}

	public String getSteeringwheelmaterial() {
		return steeringwheelmaterial;
	}

	public void setSteeringwheelmaterial(String steeringwheelmaterial) {
		this.steeringwheelmaterial = steeringwheelmaterial;
	}

	public String getSteeringwheelmultifunction() {
		return steeringwheelmultifunction;
	}

	public void setSteeringwheelmultifunction(String steeringwheelmultifunction) {
		this.steeringwheelmultifunction = steeringwheelmultifunction;
	}

	public String getSteeringwheelheating() {
		return steeringwheelheating;
	}

	public void setSteeringwheelheating(String steeringwheelheating) {
		this.steeringwheelheating = steeringwheelheating;
	}

	public String getComputerscreen() {
		return computerscreen;
	}

	public void setComputerscreen(String computerscreen) {
		this.computerscreen = computerscreen;
	}

	public String getHuddisplay() {
		return huddisplay;
	}

	public void setHuddisplay(String huddisplay) {
		this.huddisplay = huddisplay;
	}

	public String getInteriorcolor() {
		return interiorcolor;
	}

	public void setInteriorcolor(String interiorcolor) {
		this.interiorcolor = interiorcolor;
	}

	public String getRearcupholder() {
		return rearcupholder;
	}

	public void setRearcupholder(String rearcupholder) {
		this.rearcupholder = rearcupholder;
	}

	public String getSupplyvoltage() {
		return supplyvoltage;
	}

	public void setSupplyvoltage(String supplyvoltage) {
		this.supplyvoltage = supplyvoltage;
	}

}
